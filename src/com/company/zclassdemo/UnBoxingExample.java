package com.company.zclassdemo;

public class UnBoxingExample {

    public static void main(String[] args) {

        Integer i = 15;
        Float f = 15.10f;
        String s = "welcome";


        int a = i;
        float b = f;
        String c = s;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
